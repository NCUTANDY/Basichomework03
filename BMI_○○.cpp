#include <stdio.h>
#include <stdlib.h>
int main(void)
{
	float height,weight,BMI;
	printf("請輸入您的身高(單位為公尺):");
	scanf("%f",&height);
	printf("請輸入您的體重(單位為公斤):");
	scanf("%f",&weight);
	printf("= = = = = = = = = = = = = = = = = = = = = = =\n");
	BMI=weight/(height*height);
	printf("您的BMI值為:%1f\n",BMI);
	system("pause");
	return 0;
}
